# Calculadora Simples em Python

este projeto é uma calculadora simples escrita em python que permite ao usuario realizar operações matemáticas básicas entre dois números.

# Funcionalidades

A calculadora suporta as seguintes operações:

- ADIÇÃO (+): SOMA DE DOIS NÚMEROS.
- SUBTRAÇÃO (-): SUBTRAI O SEGUNDO NÚMERO DO PRIMEIRO.
- MULTIPLICAÇÃO (*): MULTIPLICA DOIS NÚMEROS.
- DIVISÃO (/): DIVIDE O PRIMEIRO NÚMERO PELO SEGUNDO.
- PORCENTAGEM (%): CALCULA A PORCENTAGEM DO PRIMEIRO NÚMERO EM RELAÇÃO AO SEGUNDO.

# Como usar 

# Uso 

O programa pedirá para inserir dois números e, em seguida, um operador.
Digite o operador desejado (+,-,*,/,%) para realizar a operação.
O resultado será exibido na tela.

# Executando em script Shell

Abra o terminal.
Navegue até o diretório onde o arquivo script.sh está localizado.
De permissão de execução ao script (se necessario) com o comando : 
chmod+x script.sh

Execute o script com o comando:
./script.sh

